# Python = High-level General Purpose Scripting Language
# Areas of programming = GUI programming, Web programming, Database programming, Network programming
# Game development, Multithreaded programming, Mobile Applications, ML/AI, Data Science
# Hybrid language (Compiler based + Interpreter based)
# Compiler = The entire source code is translated only at once into the machine code and then executed by the machine.
# Interpreter = Each source code is translated line-by-line and then executed by the machine simultaneously.
# Python uses hybrid compiler to translate the source code first into byte code and then into the machine code and then execute it.
# Python executes on PVM (Python Virtual Machine)

