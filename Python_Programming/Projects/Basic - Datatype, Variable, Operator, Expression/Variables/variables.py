# Declaration = We declare the variable by giving it a name
# Initialization = And then we initialize it by providing a data/value to it
# Initialization is done using "=" (Assignment Operator)

## Rules for declaring variables: 
# Spaces not allowed (price of = 2) ❌ (price_of = 2) ✅
# Name can contain alpha-numeric characters and underscore
# Name should start with a letter or underscore character
# Keywords should not be used
# Variables are case-sensitive (a = 10 and A = 20) both are different
# Variable names are identifiers

a = "Aditya"
b = "28"
print("There is a man named "+ a +"")
print("whose age is "+ b +"")
print(""+ a +" works as Data Analyst")

#Declaring multiple variables
a,b,c = 1,2,3
print(a,b,c)